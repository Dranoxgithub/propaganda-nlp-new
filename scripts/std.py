import math

def mean_and_variance(data):
        # Number of observations
        n = len(data)
        # Mean of the data
        mean = sum(data) / n
        print(mean)
        # Square deviations
        deviations = [(x - mean) ** 2 for x in data]
        # Variance
        variance = sum(deviations) / (n - 1)
        # standard deviation
        std = math.sqrt(variance) 
        # standard error * 1.959
        print(std/math.sqrt(n) * 1.959)
